    public static void SetLanguage(string code) =>
        _lang = code is "en" or "ru" ? code : "ru";

    public static string T(string key)
    {
        if (Strings.TryGetValue(_lang, out var d) && d.TryGetValue(key, out var s))
            return s;
        return Strings["ru"].TryGetValue(key, out var fb) ? fb : key;
    }

    public static string T(string key, params (string name, object value)[] args)
    {
        var s = T(key);
        foreach (var (name, value) in args)
            s = s.Replace("{" + name + "}", value?.ToString() ?? "");
        return s;
    }

    public static string FormatSubscriptionEndDate(int daysFromToday)
    {
        var endDate = DateTime.Today.AddDays(daysFromToday);
        var culture = _lang == "en" ? CultureInfo.GetCultureInfo("en-US") : CultureInfo.GetCultureInfo("ru-RU");
        return endDate.ToString(_lang == "en" ? "MMM d, yyyy" : "dd.MM.yyyy", culture);
    }
